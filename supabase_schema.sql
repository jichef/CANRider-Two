-- Habilitar la extensión PostGIS para geolocalización
create extension if not exists postgis;

-- Tabla de Motos
create table if not exists motorcycles (
  id uuid default gen_random_uuid() primary key,
  name text not null,
  model text,
  vin text unique,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Tabla de Telemetría (Datos en tiempo real del ESP32)
create table if not exists telemetry (
  id bigint generated by default as identity primary key,
  motorcycle_id text not null,
  timestamp timestamp with time zone default timezone('utc'::text, now()) not null,
  latitude double precision,
  longitude double precision,
  speed double precision, -- en km/h
  battery_level integer, -- porcentaje 0-100
  battery_voltage double precision,
  is_charging boolean default false,
  signal_strength integer, -- RSSI de la SIM
  location geography(point), -- Para búsquedas espaciales
  
  -- Nuevos campos para Baterías Duales
  moto_battery integer,
  moto_battery_b integer,
  bat_a_volts double precision,
  bat_a_amps double precision,
  bat_a_temp double precision,
  bat_b_volts double precision,
  bat_b_amps double precision,
  bat_b_temp double precision,
  is_charging_b boolean default false,
  location_type text,
  date text,
  is_trip_active boolean default false
);

-- Tabla para configuración manual de CAN (agnóstica a la marca)
create table if not exists can_configurations (
  id uuid default gen_random_uuid() primary key,
  motorcycle_id text not null unique,
  
  -- Batería A: Voltaje
  v_id integer default 1284,
  v_start integer default 2,
